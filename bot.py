def make_pdf(filename, text):
    c = canvas.Canvas(filename)
    width, height = 595, 842
    x = 40
    y = height - 40
    line_height = 12

    c.setFont("DejaVu", 9)

    for line in text.split("\n"):
        if y < 40:
            c.showPage()
            c.setFont("DejaVu", 9)
            y = height - 40

        c.drawString(x, y, line)
        y -= line_height

    c.save()
    return filename


